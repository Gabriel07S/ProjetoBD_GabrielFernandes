    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Captura os dados dos campos de cadastro
        String nome = txtNome.getText();
        String email = txtEmail.getText();

        // Validação dos campos obrigatórios
        if (nome.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Cadastro incompleto. Por favor, preencha todos os campos.",
                "Erro",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validação dos radio buttons para o sexo
        if (!radioMasculino.isSelected() && !radioFeminino.isSelected()) {
            JOptionPane.showMessageDialog(this,
                "Por favor, selecione o sexo.",
                "Erro",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Monta a mensagem de confirmação com os dados capturados
        String mensagem = "Concluir cadastro?\n" +
        "Deseja fazer alguma alteração nas respostas?\n\n" +
        "Nome: " + nome + "\n" +
        "Email: " + email + "\n";

        // Exibe o diálogo de confirmação com as opções "Prosseguir" e "Editar"
        int opcao = JOptionPane.showOptionDialog(
            this,
            mensagem,
            "Confirmar Cadastro",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE,
            null,
            new String[]{"Prosseguir", "Editar"},
            "Prosseguir"
        );

        if (opcao == JOptionPane.YES_OPTION) {
            try {
                // Se o usuário confirmar, abre a nova tela SakilaUI
                new SakilagUI().setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(indexui.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose(); // Fecha a tela de cadastro atual
        }
    }//GEN-LAST:event_jButton1ActionPerformed

